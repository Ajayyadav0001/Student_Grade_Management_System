    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String Rollno=jTextField1.getText();
        String Physics=jTextField7.getText();
        String Math=jTextField8.getText();
        String DBMS=jTextField9.getText();
        String Chemistry=jTextField10.getText();
        String Datastructure=jTextField11.getText();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Ajay@123");
            Statement st = con.createStatement();
            ResultSet rs =  st.executeQuery("select * from student where Rollno='"+Rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(Rollno,Physics,Math,DBMS,Chemistry,Datastructure) values('"+Rollno+"','"+Physics+"','"+Math+"','"+DBMS+"','"+Chemistry+"','"+Datastructure+"')");
                JOptionPane.showMessageDialog(null,"Result is saved successfully");
                setVisible(false);
                new insertnewresult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Student is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

